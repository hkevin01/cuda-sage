"""Triton kernel code generator for cuda-sage.

Generates production-quality Triton kernels with autotuning configs
for common GPU operations. Output is self-contained Python files ready
to import and run (requires triton + torch).
"""
from __future__ import annotations
from dataclasses import dataclass, field
from enum import Enum


class TritonOp(str, Enum):
    ELEMENTWISE = "elementwise"
    REDUCTION   = "reduction"
    MATMUL      = "matmul"
    SOFTMAX     = "softmax"
    LAYERNORM   = "layernorm"
    ATTENTION   = "attention"


class DType(str, Enum):
    FLOAT16  = "float16"
    FLOAT32  = "float32"
    BFLOAT16 = "bfloat16"


@dataclass
class TritonGenResult:
    op: TritonOp
    dtype: DType
    source: str                     # complete Python source, ready to run
    kernel_names: list[str]         # @triton.jit function names in source
    wrapper_names: list[str]        # Python wrapper function names
    autotune_configs: int           # number of autotune configs included
    notes: list[str] = field(default_factory=list)

    def __str__(self) -> str:
        return self.source


