def _tl_dtype(dtype: DType) -> str:
    return {
        DType.FLOAT16:  "tl.float16",
        DType.FLOAT32:  "tl.float32",
        DType.BFLOAT16: "tl.bfloat16",
    }[dtype]


def _activation_snippet(activation: str, accumulator_var: str = "out") -> tuple[str, str]:
    """Return (code_block, note_string) for fused activation."""
    if activation == "none":
        return "", ""
    snippets = {
        "relu":    f"    {accumulator_var} = tl.maximum({accumulator_var}, 0.0)\n",
        "gelu": (
            f"    _x = {accumulator_var}\n"
            f"    {accumulator_var} = 0.5 * _x * (1.0 + tl.math.tanh("
            f"0.7978845608 * (_x + 0.044715 * _x * _x * _x)))\n"
        ),
        "sigmoid": f"    {accumulator_var} = 1.0 / (1.0 + tl.math.exp(-{accumulator_var}))\n",
        "silu": (
            f"    {accumulator_var} = {accumulator_var} * "
            f"(1.0 / (1.0 + tl.math.exp(-{accumulator_var})))\n"
        ),
    }
    code = snippets.get(activation, "")
    return code, f"Fused {activation} activation applied before output cast."
